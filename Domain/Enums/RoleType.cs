namespace Domain.Enums;

/// <summary>
/// Перечисление ролей пользователей в системе.
/// Определяет уровень доступа и права пользователя.
/// </summary>
public enum RoleType
{
    /// <summary>
    /// Администратор системы. Полный доступ ко всем функциям.
    /// </summary>
    Admin,

    /// <summary>
    /// Менеджер. Доступ к управлению задачами и командой.
    /// </summary>
    Manager,

    /// <summary>
    /// Сотрудник. Основной пользователь системы.
    /// </summary>
    Employee,

    /// <summary>
    /// Пользователь с правами только на чтение.
    /// </summary>
    ReadOnly
}