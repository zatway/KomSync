namespace Domain.Enums;

/// <summary>
/// Перечисление приоритетов задачи.
/// Определяет важность и срочность выполнения задачи.
/// </summary>
public enum TaskPriority
{
    /// <summary>
    /// Низкий приоритет. Может быть отложен.
    /// </summary>
    Low,

    /// <summary>
    /// Средний приоритет. Стандартная важность.
    /// </summary>
    Medium,

    /// <summary>
    /// Высокий приоритет. Требует внимания в ближайшее время.
    /// </summary>
    High,

    /// <summary>
    /// Критический приоритет. Требует немедленного выполнения.
    /// </summary>
    Critical
}